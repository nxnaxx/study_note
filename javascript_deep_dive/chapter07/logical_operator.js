// OR operator
console.log(true || true);
console.log(true || false);
console.log(false || false);

// AND operator
console.log(true && true);
console.log(true && false);
console.log(false && false);

// NOT operator
console.log(!true);
console.log(!false);

// implicit coercion
console.log(!0);
console.log(!'carrot');

// short-circuit evaluation
console.log('Dog' && 'Cat');